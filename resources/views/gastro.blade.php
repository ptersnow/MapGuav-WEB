                   <h3> <b> Geleia de Guavira</b></h3>
                   
                   <ul>
                   <p> <b> Ingredientes: </b> </p>
                   <li>500 ml de suco concentrado de guabiroba</li>
                   <li>250 gramas de açúcar</li>
                   <p> <b> Modo de preparo: </b> </p> 
                   <li> Coloque um pano fino sobre uma peneira que se apoie em uma panela, ali esprema as frutinhas 
                    já lavadas, até conseguir tirar os 500 ml. Junte ao açúcar e leve ao fogo brando, mexendo sempre e 
                    retirando a espuma que surge. Em torno de 20 a 30 min estava pronto. O teste do pratinho tá valendo 
                    pra ver o ponto eu coloco o prato no congelador e pingo a geleia, se ela virar uma gelatina rapidinho,
                     então está pronto. </li>
                   </ul>
