                <div class="text-justify">
                   
                   <h3> <b> Bolo de Guavira </b></h3>
                   
                   <ul>
                   <p> <b> Ingredientes: </b> </p>
                   <li>5 ovos</li>
                   <li> 2 xícaras de açúcar mascavo</li>
                   <li>1 xícara de óleo de girassol </li>
                   <li>1 colher de sopa de amido de milho </li>
                   <li>2 xícaras de farinha de trigo </li>
                   <li>2 colheres de sopa de fermento em pó </li>
                   <li>2 xícaras de chá da casca de Guavira </li>
                   <li>2 xícaras de leite  </li>
                   <p> <b> Modo de preparo: </b> </p> 
                   <li>Separe as cascas da Guavira. Mistures as cascas limpas com as duas xícaras de leite e bata
                       no liquidificador. Em seguida, bata as claras até ficarem em ponto de neve e reserve. Depois, 
                       bata o açúcar mascavo, as gemas e o óleo. Acrescente aos poucos o leite com as cascas de Guavira.
                       Adicione as claras em neve. Bata tudo por mais alguns minutos. Adicione o fermento em pó e misture
                       com uma colher. Coloque a massa em uma forma já untada com manteiga. Se preferir, coloque cascas 
                       da fruta para assar em cima do bolo. Leve ao forno preaquecido em 180º por 40 minutos.</li>
                   </ul>
