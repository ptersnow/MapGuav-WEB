                <div class="text-justify">
                   
                   <h3> <b> Sorvete de Guavira</b></h3>
                   
                   <ul>
                   <p> <b> Ingredientes: </b> </p>
                   <li>1 lata de leite condensado</li>
                   <li>1 caixinha de creme de leite</li>
                   <li>1 lata de suco de gabiroba </li>
                   <li>1/2 l de creme de leite fresco </li>
                   <p> <b> Modo de preparo: </b> </p> 
                   <li> Primeiro deve obter o suco da gabiroba deve-se espremer uma a uma, depois disso bata com 
                    um pouco de água no liquidificador e coe, guarde esse suco. Depois bata no liquidificador o leite
                   condensado, o creme de leite e o suco de gabiroba. À parte, bater o creme de leite fresco conforme 
                   instrução da vasilha, até virar chantilly. Misture com o chantilly com o creme obtido. Coloque em um 
                   pote e leve ao congelador por cerca de 6 h. Servir com calda de amora. </li>
                   </ul>
