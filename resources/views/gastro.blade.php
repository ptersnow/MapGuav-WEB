                   <h3> <b> Rapadura de Guavira</b></h3>
                   
                   <ul>
                   <p> <b> Ingredientes: </b> </p>
                   <li>12L de caldo de cana</li>
                   <li> ½ xícara (chá) de especiaria de guavira</li>
                   </ul>

                   <ol>
                   <p> <b> Modo de preparo: </b> </p> 
                    <li>Coloque o caldo de cana em um tacho e leve-o ao fogo </li>
                    <li>Após 20 min (ou até atingir o ponto de melado) adicione a especiaria </li>
                    <li>Continue mexendo até conseguir enxergar o fundo do tacho </li>
                    <li> Desligue o fogo e com a ajuda de uma espátula mexa até esfriar </li>
                    <li> Coloque a mistura em formas e desenforme-a após 2 horas </li>
                   </ol>
