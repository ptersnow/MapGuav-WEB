                  <h3> <b> Petit gâteau guavira  </b> </h3>
                  <ul>
                  <p> <b> Ingredientes: </b> </p>
                  <li> 90g de chocolate branco</li>
                  <li> 3 claras</li>
                  <li> 5 gemas </li>
                  <li> 150g de manteiga sem sal </li>
                  <li>130g de açúcar</li>
                  <li> 300g de polpa de guavira</li>
                  <li> 25g de especiaria de guavira em pó</li>
                  <li> 100g de farinha de trigo</li>
                  </ul>

                  <ol>
                  <p> <b> Modo de preparo: </b> </p>
                  <li>Derreta o chocolate branco em banho maria. Acrescente a manteiga. Reserve. </li>
                  <li>Bata as gemas levemente e acrescente a mistura de chocolate com manteiga. </li>
                  <li> Bata as claras com o açúcar até atingir o ponto de neve. Adicione a farinha
                    peneirada, e, por último, a especiaria de guavira </li>
                  <li>Reserve por 15 minutos em geladeira </li>
                  <li>Unte uma forma utilizada para petit gâteau e acrescente a mistura.  </li>
                  <li>Asse o bolinho em forno pré-aquecido 200ºC por aproximadamente 10 minutos </li>
                  </ol>
